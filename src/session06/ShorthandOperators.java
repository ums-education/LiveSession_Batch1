package session06;

public class ShorthandOperators {

	public static void main(String[] args) {
		
		// used to UPDATE the value of a given variable by predefined number
		int num1 = 5;
		int num2 = 3;
		
		
		if (num1 > num2) {
			num2 *= 2; // num2 = num2 * 2 => num2 = 3*2 => num2 = 6
		}
		System.out.println("New value of num2: " + num2);
		
		System.out.println( -87.2D % 1  );
		
	}
	
}
