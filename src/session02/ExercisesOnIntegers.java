package session02;

public class ExercisesOnIntegers {

	public static void main(String[] args) {
		
		int num1 = 5;
		int num2 = 3;
		
		System.out.println( num1 + num2 );
		System.out.println( num1 - num2 );
		System.out.println( num1 * num2 );
		
		// division of integer numbers
		System.out.println( num1 / num2 );
		System.out.println( num2 / num1 );
		
		// modulus operator is supplement to failing division operator
		// when working with integers 
		// modulus operators works on all numeric values, including floating point numbers
		System.out.println( num1 % num2 );
		System.out.println( num2 % num1 );
		
	}
	
}
